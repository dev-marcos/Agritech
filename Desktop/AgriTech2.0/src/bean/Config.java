package bean;

public class Config {
    private int id;
    private int chave;
    private int valor;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public int getChave() {
        return chave;
    }
    public void setChave(int chave) {
        this.chave = chave;
    }
    public int getValor() {
        return valor;
    }
    public void setValor(int valor) {
        this.valor = valor;
    }
}
