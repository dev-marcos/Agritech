    public void getProgramacao() {
        try {

            ProgramacaoDAO dao = new ProgramacaoDAO();
            List<Programacao> list = dao.listAll();
            DefaultTableModel dtm = new DefaultTableModel() {
                private static final long serialVersionUID = 1L;
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            for (int i = 0; i < dtm.getRowCount(); i++) {
                dtm.removeRow(i);
            } 
            dtm.addColumn("ID");
            dtm.addColumn("Descrição");
            dtm.addColumn("Ativo");

            list.stream().forEach((programacao) -> {
                dtm.addRow(new Object[] {programacao.getId(), programacao.getDescricao(), DecodeAtivo(programacao.isAtivo())});
            });
            tbProgramacao.setModel(dtm);
        } catch (SQLException e) {
        }
    }
    
    
    
    /**
     * 
     * @param valor boolean
     * @return String
     */
    public String DecodeAtivo(boolean valor) {
        if (valor) {
            return "Sim";
        } else {
            return "Não";
        }
    }

    /**
     * 
     * @param valor String
     * @return boolean
     */
    public boolean EncodeAtivo(String valor) {
        return (valor.equals("Sim"));
    }
	
    

