    private void tbProgramacaoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbProgramacaoKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_DELETE) {
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o registro: \""+ tbProgramacao.getModel().getValueAt(tbProgramacao.getSelectedRow(),1) +"\"?", "Atenção", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                try {
                    ProgramacaoDAO dao = new ProgramacaoDAO();
                    dao.ExcluirProg((Integer) tbProgramacao.getModel().getValueAt(tbProgramacao.getSelectedRow(),0));
                    getProgramacao();
                } catch (SQLException e) {
                }

            }
        }
    }//GEN-LAST:event_tbProgramacaoKeyPressed

