    private void btAdicionarCondicaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarCondicaoActionPerformed
        // TODO add your handling code here:
        if (tfDescricao.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Informe uma descrição!");
            tfDescricao.requestFocus();
        } else if (cbAtivo.getSelectedIndex()==-1) {
            JOptionPane.showMessageDialog(null, "Escolha se a programação está ativa!");	
            cbAtivo.requestFocus();
        } else {
            progInsUp.setId(idProg);
            progInsUp.setDescricao(tfDescricao.getText());
            progInsUp.setAtivo(EncodeAtivo(cbAtivo.getSelectedItem().toString()));

            ProgramacaoDAO dao = new ProgramacaoDAO();
            if (idProg==0) {
                try {
                    dao.Inserir(progInsUp);
                    JOptionPane.showMessageDialog(null, "Adicionado com Sucesso!");
                    ClearProgramacao();
                } catch (SQLException e) {
                }
            } else {
                try {   
                    dao.Alterar(progInsUp);
                    JOptionPane.showMessageDialog(null, "Alterado com Sucesso!");
                    btAdicionarCondicao.setText("Adicionar");
                    ClearProgramacao();
                } catch (SQLException e) {
                }
            }
        }
    }//GEN-LAST:event_btAdicionarCondicaoActionPerformed

